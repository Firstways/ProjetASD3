import java.awt.Color;

public class Param {
    private int startX;
    private int startY;
    private int endX;
    private int endY;
    private Color col; 
    private String filename;

    public Param(){

    }
    public int getStartX() {
        return startX;
    }
    
    public int getStartY() {
        return startY;
    }
    
    public int getEndX() {
        return endX;
    }
    
    public int getEndY() {
        return endY;
    }
    
    public Color getCol() {
        return col;
    }
    
    public String getFilename() {
        return filename;
    }
    
}
